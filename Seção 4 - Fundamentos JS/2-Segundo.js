/*
JavaScript é uma linguagem de tipagem fraca, ou seja,
permite a mudança de tipo nas variáveis, elas são vamos
dizer, universais...
*/

let variavel = 10;
console.log(typeof variavel);

variavel = "Davi";
console.log(typeof variavel);

variavel = true;
console.log(typeof variavel);

/*
Ao executar o programa perceberemos que as variáveis em JS
permitem a mudança que muitas linguagens não permitem e por
isso ela é chama de fracamente tipada. 
*/