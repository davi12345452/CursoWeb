/*
Problema da Circunferência: pegamos uma variável raio e devemos
calcular a circunferência do círculo levando em conta que PI = 3.14
*/

let raio = 10; //Definindo variável área = 10
const PI = 3.14; //Definindo constante Pi = 3.14

let circunferencia = 2*PI*raio; //Definindo variável circunferência = 2 x pi x raio
let area = PI*(raio**2); //Definindo variável área = pi x raio x raio

console.log("Circunferência: " + circunferencia) // Imprimindo a circunferência
console.log("Área: " + area); // Imprimindo a área