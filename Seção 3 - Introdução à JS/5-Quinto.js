/*
Podemos usar o comando typeof para descobrir de qual tipo é o dado
Tipos de dados: number(float and int), string and boolean.
Porém podemos criar tipos de dados, os chamados objetos
*/

let quantidade = 70; //Definição de variável tipo number
let nome = "Davi"; //Definição de variável tipo string
let verdade = true; //Definição de variável tipo boolean

console.log(typeof quantidade); // Mostra o tipo do dado = number
console.log(typeof nome); // Mostra o tipo do dado = string
console.log(typeof verdade); // Mostra o tipo do dado = boolean